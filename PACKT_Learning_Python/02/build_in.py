# Integers
a = 12
b = 3
print("a = {}, b = {}".format(a, b))
print("a + b = {}".format(a + b))
print("a - b = {}".format(a - b))
print("a * b = {}".format(a * b))
print("a / b = {}".format(a / b))
print("a % b = {}".format(a % b))
print("a ** b = {}".format(a ** b))
print("a // b = {}".format(a // b))

# Boolean
print('int(True) = %d' % int(True))
print('int(False) = %d' % int(False))
print('bool(1) :', bool(1))
print('bool(0) :', bool(0))
print('bool(12) :', bool(12))
print('bool(-1) :', bool(-1))
print('not True :', not True)
print('not False :', not False)
print('True and True :', True and True)
print('True and False :', True and False)
print('False and True :', True and False)
print('False and False :', True and False)

# Reals
pi = 3.1415926536
radius = 4.5
area = pi * (radius ** 2)
print(area)

print(3 * 0.1 - 0.3)
print(.1 * .1 - .01)

# Complex numbers
c = 3.14 + 2.73j
print("c = {}".format(c))
print("c.real :", c.real)
print("c.imag :", c.imag)
print("c.conjugate() :", c.conjugate())
print("c * 2 =", c * 2)
